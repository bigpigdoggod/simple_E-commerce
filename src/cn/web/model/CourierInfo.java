package cn.web.model;

public class CourierInfo {

	private Integer CourierId;
	private String OrderId;
	private String CourierName;
	private String CourierPicture;
	private String CourierPhone;
	private String CourierAddress;
	
	public Integer getCourierId() {
		return CourierId;
	}
	public void setCourierId(Integer courierId) {
		CourierId = courierId;
	}
	public String getOrderId() {
		return OrderId;
	}
	public void setOrderId(String orderId) {
		OrderId = orderId;
	}
	public String getCourierName() {
		return CourierName;
	}
	public void setCourierName(String courierName) {
		CourierName = courierName;
	}
	public String getCourierPicture() {
		return CourierPicture;
	}
	public void setCourierPicture(String courierPicture) {
		CourierPicture = courierPicture;
	}
	public String getCourierPhone() {
		return CourierPhone;
	}
	public void setCourierPhone(String courierPhone) {
		CourierPhone = courierPhone;
	}
	public String getCourierAddress() {
		return CourierAddress;
	}
	public void setCourierAddress(String courierAddress) {
		CourierAddress = courierAddress;
	}
	
	
	
	
}
